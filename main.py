def mostrar_filme(indice):
    print("\n" + "="*80)
    print(f"🎬 {filmes[indice]['titulo']}".center(80))
    print("="*80)
    print("\n📖 SINOPSE:")
    print("-" * 50)
    print(filmes[indice]['sinopse'])
    print("\n🔥 RESUMO FODA:")
    print("-" * 50)
    print(filmes[indice]['resumo'])
    print("\n" + "="*80 + "\n")

def menu():
    while True:
        print("\n🎬" + " CINEMAX ".center(60, "🔥"))
        print("═" * 70)
        print("1. Ver TODOS os filmes")
        print("2. Buscar por título")
        print("3. Ver filme específico (1-10)")
        print("4. Sair")
        print("═" * 70)
        
        opcao = input("Escolha (1-4): ").strip()
        
        if opcao == "1":
            for i, filme in enumerate(filmes, 1):
                print(f"{i}. {filme['titulo']}")
            input("\n⏎ Pressione ENTER...")
            
        elif opcao == "2":
            busca = input("Digite parte do título: ").lower().strip()
            encontrados = [i for i, f in enumerate(filmes) if busca in f['titulo'].lower()]
            if encontrados:
                for i in encontrados:
                    mostrar_filme(i)
            else:
                print("😢 Nenhum filme encontrado!")
            input("\n⏎ Pressione ENTER...")
            
        elif opcao == "3":
            try:
                num = int(input("Número do filme (1-10): ")) - 1
                if 0 <= num < len(filmes):
                    mostrar_filme(num)
                else:
                    print("❌ Filme inválido!")
            except ValueError:
                print("❌ Digite um número!")
            input("\n⏎ Pressione ENTER...")
            
        elif opcao == "4":
            print("👋 Valeu por usar o CineFoda!")
            break
        else:
            print("❌ Opção inválida!")

# 🔥 RODA O PROGRAMA!
if __name__ == "__main__":
    print("Carregando CineFoda...")
    menu()